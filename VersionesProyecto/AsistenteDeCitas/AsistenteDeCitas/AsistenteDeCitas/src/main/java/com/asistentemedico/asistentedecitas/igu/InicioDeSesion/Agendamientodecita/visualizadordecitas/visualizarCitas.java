     private void leerCitasUsuario() {
                try (BufferedReader br = new BufferedReader(new FileReader("src/main/java/com/asistentemedico/asistentedecitas/persistencia/citas.txt"))) {
        String linea;
        while ((linea = br.readLine()) != null) {
            String[] campos = linea.split(", ");
            String identificacionCita = campos[2];
            if (identificacionCita.equals(identificacion)) {
                citasUsuario.agregar(campos);
            }
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
    }
    
    
